#!/usr/bin/env node
// Scans src/ for Material Symbols icon usages and writes the subset list to
// src/data/material-icons.ts. Layout.astro imports this list and appends
// `&icon_names=...` to the Google Fonts URL so only the glyphs we actually
// render ship to the browser (full font is ~300 KiB; subset is ~15–25 KiB).

import { readdirSync, readFileSync, writeFileSync, statSync } from 'node:fs';
import { join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = fileURLToPath(new URL('..', import.meta.url));
const SRC = join(ROOT, 'src');
const OUT = join(SRC, 'data', 'material-icons.ts');

const IGNORE_DIRS = new Set(['node_modules', '.astro', 'dist', '.git']);
const EXTS = new Set(['.astro', '.ts', '.tsx', '.js', '.jsx', '.mjs', '.md', '.mdx']);

function* walk(dir) {
  for (const entry of readdirSync(dir)) {
    if (IGNORE_DIRS.has(entry)) continue;
    const full = join(dir, entry);
    const s = statSync(full);
    if (s.isDirectory()) yield* walk(full);
    else if (s.isFile() && [...EXTS].some((e) => full.endsWith(e))) yield full;
  }
}

const icons = new Set();

// Patterns that capture icon names. Keep these tight to avoid false positives.
const patterns = [
  // Static span content: <span class="material-symbols-outlined ...">icon_name</span>
  /material-symbols-outlined[^<>]*>\s*([a-z][a-z0-9_]*)\s*</g,
  // Array-literal config: { icon: 'icon_name' } / iconFallback: "icon_name"
  /\bicon(?:Fallback)?\s*:\s*['"]([a-z][a-z0-9_]*)['"]/g,
  // Ternary / conditional inside material-symbols-outlined content:
  //   {foo === 'bar' ? 'icon_a' : 'icon_b'}  — catch both branches
  /material-symbols-outlined[^{]*\{[^}]*['"]([a-z][a-z0-9_]*)['"][^}]*['"]([a-z][a-z0-9_]*)['"][^}]*\}/g,
];

let filesScanned = 0;
for (const file of walk(SRC)) {
  // Skip the generated file itself
  if (file === OUT) continue;
  filesScanned++;
  const text = readFileSync(file, 'utf8');
  for (const pat of patterns) {
    for (const m of text.matchAll(pat)) {
      for (let i = 1; i < m.length; i++) {
        if (m[i]) icons.add(m[i]);
      }
    }
  }
}

// Filter out obvious non-icon tokens that might match (sanity check against
// known-bad matches from property-name false positives).
const BLOCKLIST = new Set(['true', 'false', 'null', 'undefined']);
for (const bad of BLOCKLIST) icons.delete(bad);

const sorted = [...icons].sort();
const list = sorted.join(',');

const header =
  '// AUTO-GENERATED by scripts/generate-icon-names.mjs — do not edit by hand.\n' +
  '// Run `node scripts/generate-icon-names.mjs` to refresh after adding icons.\n' +
  `// ${sorted.length} unique icons, scanned ${filesScanned} files.\n\n`;

const body = `export const MATERIAL_ICON_NAMES: readonly string[] = ${JSON.stringify(sorted, null, 2)} as const;\n\nexport const MATERIAL_ICON_NAMES_CSV = ${JSON.stringify(list)};\n`;

writeFileSync(OUT, header + body);

console.log(
  `✔ ${sorted.length} Material Symbols names written to ${relative(ROOT, OUT)} (from ${filesScanned} files)`
);
